import math

# Square Root
print(math.sqrt(25))  # Output: 5.0

# Power Function
print(math.pow(2, 3))  # Output: 8.0

# Factorial
print(math.factorial(5))  # Output: 120

# Trigonometric Functions
print(math.sin(math.radians(30)))  # Output: 0.5
print(math.cos(math.radians(60)))  # Output: 0.5

# Logarithmic Functions
print(math.log(10))  # Natural log (ln)
print(math.log10(100))  # Log base 10

# Constants
print(math.pi)  # Output: 3.141592653589793
print(math.e)  # Output: 2.718281828459045

# Greatest Common Divisor (GCD)
print(math.gcd(24, 36))  # Output: 12

# Ceiling and Floor
print(math.ceil(4.3))  # Output: 5
print(math.floor(4.7))  # Output: 4

